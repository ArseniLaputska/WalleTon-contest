import SwiftUI

extension Font {
    static func title1() -> Font {
        return .system(size: 28, weight: .semibold)
    }
    
    static func bodyRegular() -> Font {
        return .system(size: 17, weight: .regular)
    }
    
    static func bodySemibold() -> Font {
        return .system(size: 17, weight: .semibold)
    }
}
